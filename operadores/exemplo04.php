<?php

$a = 30;
$b = 55;

var_dump($a > $b);

echo "<br>";

var_dump($a < $b);

echo "<br>";

// Diferente tipo e valor
var_dump($a !== $b);

echo "<br>";

// Diferente valor
var_dump($a != $b);

echo "<br>";

// Mesmo tipo e valor
var_dump($a === $b);

echo "<br>";

// Mesmo valor
var_dump($a == $b);

echo "<br>";

var_dump($a = $b);

echo "<br>";

?>